package shapes;

public enum Color {
	RED {
        @Override
        public String toString() {
            return "Red";

        }
    },
	
	BLUE {
        @Override
        public String toString() {
            return "Blue";

        }
    },
	
	YELLOW {
        @Override
        public String toString() {
            return "Yellow";

        }
    },
	
	ORANGE {
        @Override
        public String toString() {
            return "Orange";

        }
    },
	
	GREEN {
        @Override
        public String toString() {
            return "Green";

        }
    }
}
